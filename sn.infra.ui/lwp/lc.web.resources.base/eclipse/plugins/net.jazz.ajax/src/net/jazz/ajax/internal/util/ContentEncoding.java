package net.jazz.ajax.internal.util;

import javax.servlet.http.HttpServletResponse;

public enum ContentEncoding {

	none, gzip;

	public void applyTo(HttpServletResponse response) {
		if (this == none)
			return;
		response.setHeader("Content-Encoding", name());
	}
}
